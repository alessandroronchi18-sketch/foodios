// Helper renderer per inline use (manteniamo dangerouslySetInnerHTML perché i
// path sono trusted/hardcoded in questo file). aria-hidden=true di default:
// se l'icona porta significato semantico, sovrascrivere con aria-label sul
// wrapper button/link.
export function ic(paths, size = 16) {
  return (
    <svg width={size} height={size} viewBox="0 0 24 24" fill="none"
      stroke="currentColor" strokeWidth="1.5" strokeLinecap="round" strokeLinejoin="round"
      style={{ flexShrink: 0 }} aria-hidden="true"
      dangerouslySetInnerHTML={{ __html: paths }} />
  )
}

// Componente React. Uso: <Ic name="home" size={16} />
export function Ic({ name, size = 16, color }) {
  const paths = ICONS[name]
  if (!paths) return null
  return (
    <svg width={size} height={size} viewBox="0 0 24 24" fill="none"
      stroke={color || 'currentColor'} strokeWidth="1.5"
      strokeLinecap="round" strokeLinejoin="round"
      style={{ flexShrink: 0 }} aria-hidden="true"
      dangerouslySetInnerHTML={{ __html: paths }} />
  )
}

export default ICONS
